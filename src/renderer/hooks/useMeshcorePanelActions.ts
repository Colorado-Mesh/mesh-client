      refreshContacts: runtime.refreshContacts,
      sendAdvert: runtime.sendAdvert,
      syncClock: runtime.syncClock,
      meshcoreSetChannel: runtime.setMeshcoreChannel,
      meshcoreDeleteChannel: runtime.deleteMeshcoreChannel,
